CREATE TABLE genre (
  genre_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(100) UNIQUE NOT NULL,
  description text
);

CREATE TABLE film (
  film_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title varchar(500) NOT NULL,
  description text,
  created_date date,
  age_restriction varchar(10),
  average_rating decimal(3,2) DEFAULT 0,
  added_at timestamp DEFAULT (CURRENT_TIMESTAMP)
);

CREATE TABLE film_genre (
  film_genre_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  film_id integer NOT NULL,
  genre_id integer NOT NULL
);

CREATE TABLE film_person (
  film_person_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  film_id integer NOT NULL,
  person_id integer NOT NULL,
  role varchar(100) NOT NULL
);

CREATE TABLE user_device (
  device_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id integer NOT NULL,
  type varchar(50) NOT NULL,
  model varchar(255),
  manufacturer varchar(100),
  operating_system varchar(100),
  os_version varchar(50),
  browser varchar(100),
  browser_version varchar(50),
  screen_resolution varchar(50),
  registration_date timestamp DEFAULT (CURRENT_TIMESTAMP),
  last_used_at timestamp
);

CREATE TABLE view_history (
  view_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id integer NOT NULL,
  film_id integer NOT NULL,
  device_id integer NOT NULL,
  viewed_at timestamp DEFAULT (CURRENT_TIMESTAMP),
  view_duration integer,
  view_percent decimal(5,2),
  ip_address varchar(50)
);

CREATE TABLE review (
  review_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  film_id integer NOT NULL,
  user_id integer NOT NULL,
  review_text text NOT NULL,
  film_rating integer,
  likes_count integer DEFAULT 0,
  dislikes_count integer DEFAULT 0,
  created_at timestamp DEFAULT (CURRENT_TIMESTAMP),
  updated_at timestamp
);

CREATE TABLE review_rating (
  review_rating_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  review_id integer NOT NULL,
  user_id integer NOT NULL,
  rating_type varchar(50) NOT NULL,
  rated_at timestamp DEFAULT (CURRENT_TIMESTAMP)
);

CREATE TABLE critic_review (
  critic_review_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  film_id integer NOT NULL,
  person_id integer NOT NULL,
  publication_name varchar(255),
  title varchar(500),
  review_text text NOT NULL,
  summary text,
  rating decimal(3,1),
  published_at date,
  source_url varchar(1000),
  views_count integer DEFAULT 0
);

CREATE TABLE festival (
  festival_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(255) UNIQUE NOT NULL,
  country_id integer,
  city_id integer,
  founded_year integer,
  description text,
  prestige_rating decimal(3,2)
);

CREATE TABLE award (
  award_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name varchar(255) NOT NULL,
  festival_id integer NOT NULL,
  year integer NOT NULL,
  film_id integer,
  person_id integer,
  category varchar(255),
  person_role varchar(100),
  status varchar(50) NOT NULL,
  description text
);

CREATE TABLE user_preference (
  preference_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id integer NOT NULL,
  genre_id integer,
  film_id integer,
  preference_type varchar(50) NOT NULL,
  added_at timestamp DEFAULT (CURRENT_TIMESTAMP)
);

