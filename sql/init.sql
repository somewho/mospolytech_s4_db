FROM role r
JOIN permission p
  ON (
    (r.name = 'user' AND p.action = 'read'   AND p.resource IN ('film', 'review', 'person', 'festival', 'award'))
 OR (r.name = 'user' AND p.action = 'create' AND p.resource IN ('review', 'review_rating'))
 OR (r.name = 'user' AND p.action = 'update' AND p.resource IN ('review_own', 'user_profile'))
 OR (r.name = 'user' AND p.action = 'delete' AND p.resource IN ('review_own'))
 OR (r.name = 'user' AND p.action = 'manage' AND p.resource IN ('user_preference'))
 OR (r.name = 'user' AND p.action = 'read'   AND p.resource = 'user_profile')
  );

-- moderator
