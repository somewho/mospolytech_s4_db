FROM role r
JOIN permission p
  ON r.name = 'moderator'
 AND (
    (p.action = 'read'    AND p.resource IN ('film', 'review', 'person', 'festival', 'award', 'user_profile'))
 OR (p.action = 'moderate' AND p.resource = 'review')
